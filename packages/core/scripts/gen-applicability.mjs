// Generates src/applicability/data.ts from classify/*.json (single source of truth).
// Run via:  npm run gen:applicability --workspace=a11y-assist-core
import { readFileSync, writeFileSync } from 'node:fs'
const PKG = new URL('../', import.meta.url) // packages/core/ — resolves from any cwd
const read = (rel) => JSON.parse(readFileSync(new URL(rel, PKG), 'utf8'))
const appl = read('classify/wcag-predicates.classified.json')
const verif = read('classify/wcag-verification.classified.json')
const facetsDoc = read('classify/applicability-facets.json')
const scs = read('classify/wcag-scs.source.json')

const numeric = (a,b)=>a.localeCompare(b,undefined,{numeric:true})
const scIds = scs.map(s=>s.id).sort(numeric)
const applPreds = appl.predicates.map(p=>p.id).sort()
const verifPreds = verif.predicates.map(p=>p.id).sort()
const facetKeys = Object.keys(facetsDoc.facets)

const applMeta = {}; for(const p of [...appl.predicates].sort((a,b)=>a.id.localeCompare(b.id))) applMeta[p.id]={class:p.class,scope:p.scope,definition:p.definition}
const verifMeta = {}; for(const p of [...verif.predicates].sort((a,b)=>a.id.localeCompare(b.id))) verifMeta[p.id]={tier:p.tier,scope:p.scope,definition:p.definition,axeRules:p.axe_rules||[]}
const applExpr = {}; for(const id of scIds){const c=appl.criteria.find(x=>x.sc_id===id); applExpr[id]=c.expression}
const verifExpr = {}; for(const id of scIds){const c=verif.criteria.find(x=>x.sc_id===id); verifExpr[id]=c?c.verification_expression:'true'}
const scTitle = {}; for(const s of scs.slice().sort((a,b)=>numeric(a.id,b.id))) scTitle[s.id]={title:s.title,level:s.level}
const facets = {}; for(const k of facetKeys){const v=facetsDoc.facets[k]; facets[k]={gate:v.gate,scs:v.scs,subgates:v.subgates.map(s=>({question:s.question,predicates:s.predicates}))}}
const predFacet = facetsDoc.predicate_facet

const J = o => JSON.stringify(o, null, 2)
const out = `// AUTO-GENERATED by scripts/gen-applicability.mjs from classify/*.json. Do not edit by hand.
/* eslint-disable */

export type Scope = 'component' | 'page' | 'process' | 'site'
export type ApplClass = 'auto' | 'instance' | 'human'
export type VerifTier = 'axe' | 'agent' | 'human'

export const SC_IDS = ${J(scIds)} as const
export type SCId = (typeof SC_IDS)[number]

export const APPLICABILITY_PREDICATES = ${J(applPreds)} as const
export type ApplicabilityPredicate = (typeof APPLICABILITY_PREDICATES)[number]

export const VERIFICATION_PREDICATES = ${J(verifPreds)} as const
export type VerificationPredicate = (typeof VERIFICATION_PREDICATES)[number]

export const FACET_KEYS = ${J(facetKeys)} as const
export type FacetKey = (typeof FACET_KEYS)[number]

export interface ApplMeta { class: ApplClass; scope: Scope; definition: string }
export const APPL_META: Record<ApplicabilityPredicate, ApplMeta> = ${J(applMeta)}

export interface VerifMeta { tier: VerifTier; scope: Scope; definition: string; axeRules: string[] }
export const VERIF_META: Record<VerificationPredicate, VerifMeta> = ${J(verifMeta)}

/** Boolean applicability expression per SC, over ApplicabilityPredicate + AND/OR/NOT/() + 'true'. */
export const APPL_EXPR: Record<SCId, string> = ${J(applExpr)}

/** Boolean obligation expression per SC, over VerificationPredicate. */
export const VERIF_EXPR: Record<SCId, string> = ${J(verifExpr)}

export const SC_TITLE: Record<SCId, { title: string; level: 'A' | 'AA' | 'AAA' }> = ${J(scTitle)}

export interface SubGate { question: string; predicates: ApplicabilityPredicate[] }
export interface Facet { gate: string; scs: SCId[]; subgates: SubGate[] }
export const FACETS: Record<FacetKey, Facet> = ${J(facets)}

/** Facet of each non-auto applicability predicate (auto predicates are not gated). */
export const PREDICATE_FACET: Partial<Record<ApplicabilityPredicate, FacetKey>> = ${J(predFacet)}
`
writeFileSync(new URL('src/applicability/data.ts', PKG), out)
console.log(`wrote src/applicability/data.ts — ${scIds.length} SCs, ${applPreds.length} appl preds, ${verifPreds.length} verif preds, ${facetKeys.length} facets`)
